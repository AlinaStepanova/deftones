        <div><strong>Deftones - альтернативний гурт з Сакраменто, Каліфорнія, США. <div>До складу входять: Чіно Морено, Ейб Каннінгем, Стефан Карпентер і Франк Делгадо.</div></strong></div><br>
        
        <div align="justify">
        <div></div>Перший альбом <kbd>Adrenaline</kbd> вийшов в 1995 році, на рік пізніше дебютного диска <dfn>Korn</dfn>. Ці гурти вважаються засновниками ню-металу, та все ж, <em>Deftones</em> грали набагато раніше самих <dfn>Korn</dfn>, в кінці 1980-х років - андеграундні тури без лейблу, хоча гурт <dfn>Korn</dfn> і залишається куди більш комерційно вигідним проектом. Саме стилістика і манери <em>Deftones</em> були перероблені <dfn>Limp Bizkit</dfn> (гурт нерідко висловлювала вдячність Deftones «за натхнення» як в своїх буклетах, так і в декотрих текстах пісень) і <dfn>Linkin Park</dfn>, найбільш затребуваними, на даний момент, колективами подібного роду. І якщо перші виконували на концертах <kbd>«7 Words»</kbd> (з дебютного диска), то другі - «My Own Summer (Shove It)» (альбом <kbd>Around the Fur</kbd>, 1997). Самі музиканти <em>Deftones</em> просять не зараховувати їх до якогось конкретного стилю.</div><br> 
        </div>
    
